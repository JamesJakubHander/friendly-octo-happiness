// helper: window.t('latestNews') or window.t('inDays', 4)
window.t = function (key, ...args) {
  const dict = window.PP_L10N[window.PP_LANG] || window.PP_L10N.en;
  // dotted path: pos.QB
  const parts = key.split('.');
  let v = dict;
  for (const p of parts) v = v?.[p];
  if (typeof v === 'function') return v(...args);
  return v ?? key;
};

